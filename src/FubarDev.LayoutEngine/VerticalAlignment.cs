namespace FubarDev.LayoutEngine;

/// <summary>
/// Specifies the vertical alignment of a layout item.
/// </summary>
public enum VerticalAlignment
{
    /// <summary>
    /// Aligns the item to the top.
    /// </summary>
    Top,
    /// <summary>
    /// Centers the item vertically.
    /// </summary>
    Center,
    /// <summary>
    /// Aligns the item to the bottom.
    /// </summary>
    Bottom,
    /// <summary>
    /// Stretches the item to fill the available vertical space.
    /// </summary>
    Fill,
}
