namespace FubarDev.LayoutEngine;

/// <summary>
/// Specifies the horizontal alignment of a layout item.
/// </summary>
public enum HorizontalAlignment
{
    /// <summary>
    /// Aligns the item to the left.
    /// </summary>
    Left,
    /// <summary>
    /// Centers the item horizontally.
    /// </summary>
    Center,
    /// <summary>
    /// Aligns the item to the right.
    /// </summary>
    Right,
    /// <summary>
    /// Stretches the item to fill the available horizontal space.
    /// </summary>
    Fill,
}
